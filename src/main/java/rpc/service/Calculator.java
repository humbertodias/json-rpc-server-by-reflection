package rpc.service;

public class Calculator {

    public long multiply(Long a, Long b) {
        return a * b;
    }

    public long sum(Long a, Long b) {
        return a + b;
    }

    public long minus(Long a, Long b) {
        return a - b;
    }

    public long divide(Long a, Long b) {
        return a / b;
    }
}
